/* Escreva um algoritmo que percorra os números impares de 2 a 19, e
se o número for ímpar, exiba o número; */

package aula8;

public class NumerosImpar {
    public static void main(String[] args) {
        
        for(int cont = 3; cont <= 19; cont+=2) {
            System.out.println(cont);
        }
    }
}
